package LearnJava.Scopeinjava;

public class Scope {
    public static void main(String[]args){

    //Common Escape Sequences in Java

//  \n - Newline: Moves the cursor to the next line.
    System.out.println("Hello\nWorld");  // Output:    Hello
                                           //            World
//  \t - Tab: Adds a tab space.
    System.out.println("Name:\tJohn");  //Output: Name:    John


//  \\ - Backslash: Used to print a backslash character.
    System.out.println("C:\\Program Files"); //Output: C:\Program Files


//  \" - Double quote: Used to print a double quote within a string.
    System.out.println("She said, \"Hello!\""); //Output: She said, "Hello!"


//  \' - Single quote: Used to print a single quote.
    System.out.println("It\'s a beautiful day"); //Output: It's a beautiful day
}
}
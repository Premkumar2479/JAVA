// Data Type - char

// The char data type is used to store a single character, like a letter or a symbol.
// A char takes up 2 bytes of memory, which is equal to 16 bits. This allows it to represent a wide variety of characters from different languages and symbols.
// You can use char to represent any character by enclosing it in single quotes, like 'A', 'b', or '3'.

// Syntax:
// char variableName = 'A';

package LearnJava.Datatype;

public class Char {
    public static void main(String[] args) {
        char grade = 'A'; // Declaring and assigning a char variable
        System.out.println("Student Grade: " + grade);
    }
}

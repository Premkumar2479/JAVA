// Data Type - double

// The double data type is also used for decimal numbers, but it can hold much larger and more precise values than float.
// It is great for representing very large or very small numbers or when precise calculations are needed (like in scientific calculations).
// A double takes up 8 bytes of memory, which is equal to 64 bits, giving it more precision.


// Syntax:
// double variableName = value;

package LearnJava.Datatype;

public class Double {
    public static void main(String[] args) {
        double pi = 3.1415926535; // Declaring and assigning a double variable
        System.out.println("Value of Pi: " + pi);
    }
}
